export function MobileBottomNav() {
  const pathname = usePathname();
  const { data: session } = useSession();

  // Don't show on auth pages
  if (!session || pathname?.startsWith("/login") || pathname?.startsWith("/register")) {
    return null;
  }

  return (
    <nav 
      className="fixed bottom-0 left-0 right-0 z-40 md:hidden bg-white dark:bg-zinc-900 border-t border-zinc-200 dark:border-zinc-800 safe-area-pb"
      aria-label="Mobile navigation"
    >
      <div className="flex items-center justify-around h-16 px-2">
        {navItems.map((item) => {
          const isActive = pathname === item.href || (item.href !== "/dashboard" && pathname?.startsWith(item.href));
          
          if (item.isPrimary) {
            return (
              <Link
                key={item.href}
                href={item.href}
                className="flex items-center justify-center -mt-4 h-14 w-14 rounded-full bg-gradient-to-r from-blue-600 to-indigo-600 text-white shadow-lg shadow-blue-500/30 active:scale-95 transition-transform"
                aria-label={item.label}
              >
                {item.icon}
              </Link>
            );
          }

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`
                flex flex-col items-center justify-center flex-1 h-full py-2
                transition-colors
                ${isActive 
                  ? "text-blue-600 dark:text-blue-400" 
                  : "text-zinc-500 dark:text-zinc-400 active:text-zinc-900 dark:active:text-zinc-100"
                }
              `}
              aria-current={isActive ? "page" : undefined}
            >
              <span className="mb-1">
                {isActive ? item.iconFilled : item.icon}
              </span>
              <span className="text-[10px] font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

